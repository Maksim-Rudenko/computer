"""
создай код на python в соответствии с условием
задана матрица размером NхM. Программа должна:
1) вводить размерность и элементы матрицы;
2) определить, сколько положительных элементов расположено в строках 
с нечетными номерами;
3) выводить исходную матрицу и результат вычислений.

"""

# Ввод размерности матрицы
N = int(input("Введите количество строк матрицы: "))
M = int(input("Введите количество столбцов матрицы: "))

# Ввод элементов матрицы
matrix = []
for i in range(N):
    row = list(map(int, input("Введите элементы строки номер {} матрицы через пробел: ".format(i+1)).split()))
    matrix.append(row)

# Вычисление количества положительных элементов в строках с нечетными номерами
count = 0
for i in range(N):
    if (i + 1) % 2 != 0:  # Нечетный номер строки (индексация в Python начинается с 0, поэтому добавляем 1)
        for j in range(M):
            if matrix[i][j] > 0:  # Положительный элемент
                count += 1

# Вывод исходной матрицы и результата вычислений
print("Исходная матрица:")
for row in matrix:
    print(row)
print("Количество положительных элементов в строках с нечетными номерами:", count)